# creando diccionarios con dict
diccionario = dict(nombre="joaquin", apellido="mauriño", edad=23)

# que es lo mismo que directamente escribir:
diccionario = {"nombre": "joaquin", "apellido": "mauriño", "edad": 23}

# creando diccionario con fromkeys([])  Values => None
diccionario = dict.fromkeys(["Nombre", "Apellido"])
